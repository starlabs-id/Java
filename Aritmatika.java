public class Aritmatika
{
	public Aritmatika()
	{}
	public static double tambah(double x, double y)
	{
		double hasil;
		hasil = x + y;
		return hasil;
	}
	public static double kurang(double x, double y)
	{
		double hasil;
		hasil = x - y;
		return hasil;
	}
	public static double kali(double x, double y)
	{
		double hasil;
		hasil = x * y;
		return hasil;
	}
	public static double bagi(double x, double y)
	{
		double hasil;
		hasil = x / y;
		return hasil;
	}
}
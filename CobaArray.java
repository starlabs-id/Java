class CobaArray
{
	public static void main(String[] args)
	{
		String[] kota;
		kota = new String[5];

		kota[0] = "Jakarta";
		kota[1] = "Denpasar";
		kota[2] = "Bandung";
		kota[3] = "Yogyakarta";
		kota[4] = "Surabaya";
		System.out.println("Index ke-0: " + kota[0]);
		System.out.println("Index ke-1: " + kota[1]);
		System.out.println("Index ke-2: " + kota[2]);
		System.out.println("Index ke-3: " + kota[3]);
		System.out.println("Index ke-4: " + kota[4]);
	}
}